encrypted=`getprop ro.crypto.state`
decrypt_state=`getprop vold.decrypt`

# Only restart media for encrypted devices, and make sure it is after
# decryption is complete so we don't restart media early on for no reason
if [ "$encrypted" = "encrypted" ] && [ "$decrypt_state" = "trigger_restart_framework" ]
then
  sleep 5
  restart media
fi
